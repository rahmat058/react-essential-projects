function pad(n, width = 2) {
  return String(n).padStart(width, "0");
}

function isoDate(year, month, day) {
  return new Date(Date.UTC(year, month - 1, day)).toISOString();
}

const data = [];

products.forEach((p, i) => {
  const [title, slug, description, category, tags, popularity, brand, price] =
    p;
  data.push({
    id: `prd_01QL${pad(i + 1)}${slug.slice(0, 8).toUpperCase().replace(/-/g, "")}`,
    entityType: "product",
    title,
    slug,
    description,
    category,
    tags,
    popularityScore: popularity,
    isActive: true,
    thumbnailUrl: null,
    createdAt: isoDate(2023, (i % 12) + 1, (i % 28) + 1),
    updatedAt: isoDate(2025, 11, (i % 28) + 1),
    metadata: {
      sku: `${brand.slice(0, 3).toUpperCase()}-${pad(i + 1)}`,
      price,
      currency: "USD",
      stockQuantity: 20 + ((i * 17) % 200),
      brand,
    },
  });
});

