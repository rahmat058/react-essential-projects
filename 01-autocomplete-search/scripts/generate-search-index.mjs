const dataset = {
  meta: {
    schemaVersion: "1.0.0",
    collection: "search_index",
    description: "Mock search index — mirrors production DB export format",
    count: data.length,
    breakdown: {
      product: data.filter((d) => d.entityType === "product").length,
      city: data.filter((d) => d.entityType === "city").length,
      user: data.filter((d) => d.entityType === "user").length,
    },
    lastUpdated: new Date().toISOString(),
  },
  data,
};

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, JSON.stringify(dataset, null, 2), "utf8");
console.log(`Generated ${data.length} items → ${outPath}`);
